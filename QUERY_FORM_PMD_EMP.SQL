

SELECT EMP_OFFICE_NAME,EMP_CODE,TITLE_DESC
FROM
(
SELECT A.EMP_OFFICE_NAME,A.EMP_CODE,B.TITLE_DESC,A.JOB_GRADE_CODE
FROM PMIS_EMP A, PMIS_JOB_TITLE B
WHERE   A.JOB_TITLE_CODE        =       B.TITLE_CODE
AND     A.COMPANY_ID            =       '00001'
AND     A.JOB_CATEGORY NOT IN('4','5','7','10','11')
AND     A.JOB_GRADE_CODE IN ('M-4','M-5','M-6','M-7','M-8','M-9','M-10')
AND     A.DEPT_CODE IN ('00024','00030')
UNION
SELECT A.EMP_OFFICE_NAME,A.EMP_CODE,B.TITLE_DESC,A.JOB_GRADE_CODE
FROM PMIS_EMP A, PMIS_JOB_TITLE B
WHERE   A.JOB_TITLE_CODE        =       B.TITLE_CODE
AND     A.COMPANY_ID            =       '00001'
AND     A.JOB_CATEGORY NOT IN('4','5','7','10','11')
AND     A.EMP_CODE NOT IN ('PE02013')
AND     A.JOB_GRADE_CODE IN ('M-11','M-12','M-13','M-14','M-15','M-16')
AND     A.DEPT_CODE IN ('00030','00024')
)
ORDER BY 2;

