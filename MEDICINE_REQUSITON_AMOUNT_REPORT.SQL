
SELECT A.ISSUE_TO_EMP,B.EMP_OFFICE_NAME,C.TITLE_DESC,SUM(A.TOT_PRICE)TOT_PRICE FROM
(SELECT ISSUE_TO_EMP,ISSUE_NO,P_CODE,PCAT_CODE,P_DESC, SUM(NVL(DISP_QNTY,0))DISP_QNTY,PRICE,DISP_QNTY*PRICE TOT_PRICE
FROM (
SELECT TRUNC(A.ENTER_DT,'MON') REQ_MON,ISSUE_TO_EMP,a.ISSUE_NO,B.P_CODE,D.PCAT_CODE,D.P_DESC,B.STORE_TYPE,SUM(B.DISP_QNTY-NVL(C.RET_QNTY,0)) DISP_QNTY, B.UTP+B.UVP PRICE
FROM 
CSMS_OPL.WELFARE_USER_REQ_MAIN A,CSMS_OPL.WELFARE_USER_REQ_ITEM B,WELFARE_RETURN_RECV C,
(SELECT DISTINCT P_CODE,PCAT_CODE,P_DESC,COMPANY_ID FROM OPL.PRODUCT WHERE COMPANY_ID='00001'
UNION
SELECT DISTINCT P_CODE,PCAT_CODE,P_DESC,'00002' COMPANY_ID FROM OSL.PRODUCT@OSL
UNION
SELECT DISTINCT P_CODE,PCAT_CODE,P_DESC,'00010' COMPANY_ID FROM OHNL.PRODUCT@OHL) D
WHERE A.ISSUE_NO = B.ISSUE_NO
AND B.P_CODE=D.P_CODE
AND A.UTYPE='S'
AND A.ISSUE_TO_EMP IN (SELECT EMPNO FROM SD_RM
UNION
SELECT EMPNO FROM SD_RM_SP
UNION
SELECT EMPNO FROM SD_AM
UNION
SELECT EMPNO FROM SD_AM_SP
UNION
SELECT EMPNO FROM SD_SM
UNION
SELECT 'PE03484' FROM DUAL
UNION
SELECT 'PE04284' FROM DUAL
UNION
SELECT 'PE03477' FROM DUAL)
AND B.STORE_TYPE=D.COMPANY_ID
AND B.ISSUE_NO = C.REQ_NO(+)
AND B.P_CODE=C.P_CODE(+)
AND B.DISP_STAT='Y'
--and  a.ISSUE_TO_EMP='PE00654'
AND TRUNC(A.ENTER_DT,'YEAR')='01-JAN-2024'
GROUP BY TRUNC(A.ENTER_DT,'MON'),ISSUE_TO_EMP,a.ISSUE_NO,B.P_CODE,D.PCAT_CODE,D.P_DESC,B.STORE_TYPE,B.UTP+B.UVP
ORDER BY REQ_MON DESC
)
GROUP BY ISSUE_TO_EMP,ISSUE_NO,P_CODE,PCAT_CODE,P_DESC, PRICE,DISP_QNTY*PRICE
ORDER BY P_DESC) A, PMIS_EMP_ALL_V B,PMIS_JOB_TITLE_ALL_V C
WHERE ISSUE_TO_EMP=B.EMP_CODE
AND   B.JOB_TITLE_CODE=C.TITLE_CODE
AND  B.COMPANY_ID=C.COMPANY_ID
GROUP BY  A.ISSUE_TO_EMP,B.EMP_OFFICE_NAME,C.TITLE_DESC
ORDER BY TITLE_DESC;

