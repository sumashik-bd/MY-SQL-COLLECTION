    DECLARE
    CURSOR C1 IS
            SELECT TRUNC(A.FOR_MON,'MON') FOR_MON,B.P_N_CODE,B.PROD_BATCH,C.P_DESC, SUM(DISP_QNTY) STOCK_QNTY,A.CYCLE FROM 
            DISP_SAMP_MAIN A, DISP_SAMP_ITEM B,SAM_PRODUCT C
            WHERE A.DISP_SLNO =B.DISP_SLNO
            AND B.P_N_CODE  = C.P_N_CODE
            AND A.DISP_SLNO ='SS055716NK'
            GROUP BY TRUNC(A.FOR_MON,'MON'),B.P_N_CODE,B.PROD_BATCH,C.P_DESC,A.CYCLE
            ORDER BY C.P_DESC;
    BEGIN
        FOR REC IN C1 LOOP
            UPDATE  PBMS_AVT.SAM_PROD_STOCK
            SET STK_QNTY    = NVL(STK_QNTY,0) + REC.STOCK_QNTY,
                OUT_QNTY    = NVL(OUT_QNTY,0) - REC.STOCK_QNTY
            WHERE P_N_CODE  = REC.P_N_CODE
            AND PROD_BATCH  = REC.PROD_BATCH;                    
        END LOOP;
        --COMMIT;
    END;

