  DECLARE
            CURSOR C1 IS
                    SELECT TRUNC(A.FOR_MON,'MON') FOR_MON,B.PPM_CODE,C.PPM_DESC, SUM(DISP_QNTY) STOCK_QNTY,A.CYCLE FROM 
                    DISP_PPM_MAIN A, DISP_PPM_ITEM B,PPM_INVENTORY_ITEM C
                    WHERE A.DISP_SLNO =B.DISP_SLNO
                    AND B.PPM_CODE  = C.PPM_CODE
                    AND TRUNC(A.DISP_DATE) ='24-JAN-2026'
                    --AND TRUNC(A.FOR_MON,'MON') ='01-FEB-2026'
                    --AND DISP_TYPE='GN'
                    --AND A.GIFT_TYPE ='P'
                    AND B.PPM_CODE ='CDSDBK0004'
                    GROUP BY TRUNC(A.FOR_MON,'MON'),B.PPM_CODE,C.PPM_DESC,A.CYCLE
                    ORDER BY C.PPM_DESC;
            BEGIN
                FOR REC IN C1 LOOP
                    UPDATE  PBMS_OPL.PPM_STOCK
                    SET STK_QNTY    = NVL(STK_QNTY,0) + REC.STOCK_QNTY,
                        OUT_QNTY    = NVL(OUT_QNTY,0) - REC.STOCK_QNTY
                    WHERE PPM_CODE  = REC.PPM_CODE;                    
                END LOOP;
                COMMIT;
            END;


