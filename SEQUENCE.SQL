------DROP-------
DROP SEQUENCE SMIS.PROJ_CODE;

-------CREATE SEQUENCE------

CREATE SEQUENCE PROJ_CODE
    INCREMENT BY 1
    START WITH 1
    MINVALUE 1
    MAXVALUE 1000
    CYCLE
    CACHE 2;

-----GENERATE SEQUENCE------

SELECT 'DH-' || TO_CHAR(SYSDATE,'YYYYMMDD')|| LPAD(TO_NUMBER(NVL(PROJ_CODE.NEXTVAL,'0'))+1,4,'0') FROM DUAL;

SELECT TO_CHAR(SYSDATE,'YYYYDDMM')||LPAD(TO_NUMBER(NVL(MAX(SUBSTR(INV_NO,9,20)),'0'))+1,10,'0') FROM INVOICE_MST;

SELECT LPAD(TO_NUMBER(NVL(PROJ_CODE.NEXTVAL,'0'))+1,5,'0') FROM DUAL;


