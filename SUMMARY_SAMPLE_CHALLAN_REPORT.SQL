       
        SELECT TYPE_DESC,FOR_MON,A.P_N_CODE,A.PROD_BATCH,P_DESC,PRV_QNTY,CHALL_QNTY,CUR_QNTY,CYCLE
        FROM
        (
        SELECT TYPE_DESC,TRUNC(A.FOR_MON,'MON') FOR_MON,B.P_N_CODE,B.PROD_BATCH,C.P_DESC, SUM(DISP_QNTY) CHALL_QNTY,A.CYCLE FROM 
        DISP_SAMP_MAIN A, DISP_SAMP_ITEM B,SAM_PRODUCT C, SAMP_PLAN_TYPE D
        WHERE A.DISP_SLNO =B.DISP_SLNO
        AND B.P_N_CODE  = C.P_N_CODE
        AND A.DISP_TYPE = D.TYPE_CODE
        AND TRUNC(A.FOR_MON,'MON') ='01-JUL-2026'
        AND DISP_TYPE='GA'
        GROUP BY TYPE_DESC,TRUNC(A.FOR_MON,'MON'),B.P_N_CODE,B.PROD_BATCH,C.P_DESC,A.CYCLE) A,
        (
            SELECT P_N_CODE,PROD_BATCH,STK_QNTY PRV_QNTY FROM SAM_STOCK_CHALL_PRE_INFOR
            WHERE FF_TYPE ='C'
            AND MNYR ='01-JUL-2026'
            
          ) B,
          (
            SELECT P_N_CODE,PROD_BATCH,STK_QNTY CUR_QNTY FROM SAM_STOCK_CHALL_CUR_INFOR
            WHERE FF_TYPE ='C'
            AND MNYR ='01-JUL-2026'
           ) C
       WHERE A.P_N_CODE = B.P_N_CODE
       AND  A.PROD_BATCH = B.PROD_BATCH
       AND  B.P_N_CODE  = C.P_N_CODE
       AND   B.PROD_BATCH = C.PROD_BATCH
           