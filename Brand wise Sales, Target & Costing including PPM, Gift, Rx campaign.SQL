-- PPM AND GIFT EXPENSE

SELECT MNYR, DECODE(GIFT_TYPE,'P','PPM','G','Gift',GIFT_TYPE)GIFT_TYPE, BRAND_CODE, BRAND_NAME, SUM(NVL(PPM_GIFT_COST,0)) PPM_GIFT_COST
FROM(
SELECT A.MNYR, A.PPM_CODE, A.GIFT_TYPE, A.BRAND_CODE, A.BRAND_NAME, A.PPM_DESC, ROUND(A.DISP_QNTY*B.RATE) PPM_GIFT_COST 
FROM
(
SELECT A.FOR_MON MNYR, B.PPM_CODE, IS_PPM_GIFT GIFT_TYPE , D.BRAND_CODE, E.BRAND_NAME, PPM_DESC, SUM(NVL(DISP_QNTY,0)) DISP_QNTY, 'OPL' ID1
FROM  PBMS_OPL.DISP_PPM_MAIN A, PBMS_OPL.DISP_PPM_ITEM B, PBMS_OPL.PPM_MAIN_TYPE C, PBMS_OPL.PPM_INVENTORY_ITEM D, OPL.BRAND E,(SELECT DISTINCT PPM_CODE FROM PPM_YEARLY_PLAN WHERE REQ_FOR_DEPT = 'PMD') F
WHERE A.DISP_SLNO            = B.DISP_SLNO
AND   SUBSTR(B.PPM_CODE,1,2) = C.PPM_M_CODE
AND   B.PPM_CODE             = D.PPM_CODE
AND   SUBSTR(D.PPM_CODE,5,2) = E.BRAND_CODE
AND   B.PPM_CODE             = F.PPM_CODE
AND   TRUNC(FOR_MON,'MON')   BETWEEN '01-JAN-2025' AND '01-MAR-2025'
GROUP BY A.FOR_MON, B.PPM_CODE, IS_PPM_GIFT , D.BRAND_CODE, E.BRAND_NAME, PPM_DESC
UNION ALL
SELECT A.FOR_MON MNYR, B.PPM_CODE, IS_PPM_GIFT GIFT_TYPE , D.BRAND_CODE, E.BRAND_NAME, PPM_DESC, SUM(NVL(DISP_QNTY,0)) DISP_QNTY, 'OHNL' ID1
FROM  PBMS_OPL.DISP_PPM_MAIN A, PBMS_OPL.DISP_PPM_ITEM B, PBMS_OHNL.PPM_MAIN_TYPE@OHL C, PBMS_OHNL.PPM_INVENTORY_ITEM@OHL D, OPL.BRAND E,(SELECT DISTINCT PPM_CODE FROM PBMS_OHNL.PPM_YEARLY_PLAN@OHL WHERE REQ_FOR_DEPT = 'PMD') F
WHERE A.DISP_SLNO            = B.DISP_SLNO
AND   SUBSTR(B.PPM_CODE,1,2) = C.PPM_M_CODE
AND   B.PPM_CODE             = D.PPM_CODE
AND   SUBSTR(D.PPM_CODE,5,2) = E.BRAND_CODE
AND   B.PPM_CODE             = F.PPM_CODE
AND   TRUNC(FOR_MON,'MON')   BETWEEN '01-JAN-2025' AND '01-MAR-2025'
GROUP BY A.FOR_MON, B.PPM_CODE, IS_PPM_GIFT , D.BRAND_CODE, E.BRAND_NAME, PPM_DESC
) A,
(
SELECT PPM_CODE, NVL(MAX(PO_RATE),0) RATE, 'OPL' ID1
FROM PBMS_OPL.PPM_GRN
GROUP BY PPM_CODE
UNION ALL
SELECT PPM_CODE, NVL(MAX(PO_RATE),0) RATE, 'OHNL' ID1
FROM PBMS_OHNL.PPM_GRN@OHL
GROUP BY PPM_CODE
) B
WHERE A.PPM_CODE = B.PPM_CODE(+)
AND   A.ID1      = B.ID1
)
GROUP BY MNYR, GIFT_TYPE, BRAND_CODE, BRAND_NAME
ORDER BY BRAND_NAME;





-- SAMPLE EXPENSE

SELECT A.MNYR, 'SAMPLE' TYPE, C.BRAND_CODE, C.BRAND_NAME, SUM(NVL(SAMPLE_COST,0)) SAMPLE_COST
FROM
(
SELECT FOR_MON MNYR, SUBSTR(B.P_N_CODE,1,4) P_CODE, ROUND(SUM(NVL(DISP_QNTY,0)*NVL(TOT_SAMP_COST,0))) SAMPLE_COST
FROM PBMS_OPL.DISP_SAMP_MAIN A, PBMS_OPL.DISP_SAMP_ITEM B, PBMS_OPL.SAMP_COST_PBMS_OPL_VW C
WHERE A.DISP_SLNO = B.DISP_SLNO
AND   B.P_N_CODE  = C.P_N_CODE
AND   TRUNC(FOR_MON,'MON')  BETWEEN '01-JAN-2025' AND '01-MAR-2025'
GROUP BY FOR_MON, SUBSTR(B.P_N_CODE,1,4)
) A, PRODUCT B, BRAND C
WHERE A.P_CODE     = B.P_CODE(+)
AND   B.BRAND_CODE = C.BRAND_CODE(+)
GROUP BY A.MNYR, C.BRAND_CODE, C.BRAND_NAME
UNION
SELECT A.MNYR, 'SAMPLE' TYPE, C.BRAND_CODE, C.BRAND_NAME, SUM(NVL(SAMPLE_COST,0)) SAMPLE_COST
FROM
(
SELECT FOR_MON MNYR, SUBSTR(B.P_N_CODE,1,4) P_CODE, ROUND(SUM(NVL(DISP_QNTY,0)*NVL(TOT_SAMP_COST,0))) SAMPLE_COST
FROM PBMS_OPL.DISP_SAMP_MAIN A, PBMS_OPL.DISP_SAMP_ITEM B, PBMS_OHNL.SAMP_COST_PBMS_OHNL_VW@OHL C
WHERE A.DISP_SLNO = B.DISP_SLNO
AND   B.P_N_CODE  = C.P_N_CODE
AND   TRUNC(FOR_MON,'MON')  BETWEEN '01-JAN-2025' AND '01-MAR-2025'
GROUP BY FOR_MON, SUBSTR(B.P_N_CODE,1,4)
) A, PRODUCT B, BRAND C
WHERE A.P_CODE     = B.P_CODE(+)
AND   B.BRAND_CODE = C.BRAND_CODE(+)
GROUP BY A.MNYR, C.BRAND_CODE, C.BRAND_NAME
ORDER BY BRAND_NAME;





-- RX EXPENSE

DROP TABLE XXXXX_IOU_RX PURGE;

CREATE TABLE XXXXX_IOU_RX
NOLOGGING
PARALLEL
AS
SELECT SUB_SLNO, DEPOT_CODE, SUM(NVL(EXP_RX,0)) EXP_RX
FROM(
SELECT RX_SLNO SUB_SLNO, DEPOT_CODE, CASE WHEN NVL(EXPENSE_VALUE,0) = 0 THEN NVL(IOU_VALUE,0) ELSE NVL(EXPENSE_VALUE,0) END EXP_RX
FROM GL_OPL.IOU_INFO
WHERE RX_SLNO IS NOT NULL
)
GROUP BY SUB_SLNO, DEPOT_CODE;




DROP TABLE XXXXX_IOU_RX_TOT_VAL PURGE;

CREATE TABLE XXXXX_IOU_RX_TOT_VAL
NOLOGGING
PARALLEL
AS
SELECT DISTINCT TRUNC(A.MNYR,'MON')  MNYR, B.MST_SLNO, B.SUB_SLNO, B.DEPOT_CODE, C.EXP_RX TOT_COST
FROM PBMS_OPL.RX_GEN_CAMP_MST A, PBMS_OPL.RX_GEN_CAMP_DTL B, (SELECT DISTINCT SUB_SLNO, DEPOT_CODE, EXP_RX FROM XXXXX_IOU_RX) C
WHERE A.MST_SLNO = B.MST_SLNO
AND   B.SUB_SLNO = C.SUB_SLNO
AND   TRUNC(A.MNYR,'MON') BETWEEN '01-JAN-2025' AND '01-MAR-2025'
AND   B.VC_NO IS NOT NULL
UNION ALL
SELECT DISTINCT TRUNC(A.MNYR,'MON')  MNYR, B.MST_SLNO, B.SUB_SLNO, B.DEPOT_CODE, NVL(B.PARTI_DOC,0)*NVL(UNIT_COST,0) TOT_COST
FROM PBMS_OPL.RX_GEN_CAMP_MST A, PBMS_OPL.RX_GEN_CAMP_DTL B
WHERE A.MST_SLNO = B.MST_SLNO
AND   TRUNC(A.MNYR,'MON') BETWEEN '01-JAN-2025' AND '01-MAR-2025'
AND   B.VC_NO IS NOT NULL
AND   NOT EXISTS(SELECT 1 FROM XXXXX_IOU_RX C WHERE C.SUB_SLNO = B.SUB_SLNO);




SELECT MNYR, 'RX' TYPE, A.BRAND_CODE, A.BRAND_NAME, SUM(NVL(RX_COST,0)) RX_COST
FROM(
SELECT A.MNYR, A.BRAND_CODE, B.BRAND_NAME, A.RX_COST
FROM(
SELECT A.MNYR, B.BRAND_CODE, ROUND(SUM(NVL(A.TOT_COST,0)/NVL(C.TOT_BRAND,0))) RX_COST
FROM
(
SELECT MNYR, MST_SLNO, SUB_SLNO, DEPOT_CODE, TOT_COST
FROM XXXXX_IOU_RX_TOT_VAL
) A,
(
SELECT DISTINCT MST_SLNO, BRAND_CODE
FROM PBMS_OPL.RX_GEN_CAMP_MST
WHERE TRUNC(MNYR,'YEAR') = TRUNC(SYSDATE-1,'YEAR')
) B,
(
SELECT MST_SLNO, COUNT(DISTINCT BRAND_CODE) TOT_BRAND
FROM PBMS_OPL.RX_GEN_CAMP_MST
WHERE TRUNC(MNYR,'YEAR') = TRUNC(SYSDATE-1,'YEAR')
GROUP BY MST_SLNO
) C
WHERE A.MST_SLNO = B.MST_SLNO
AND   A.MST_SLNO = C.MST_SLNO
GROUP BY A.MNYR, B.BRAND_CODE
) A, BRAND B
WHERE A.BRAND_CODE = B.BRAND_CODE
) A
GROUP BY A.MNYR, A.BRAND_CODE, A.BRAND_NAME
ORDER BY BRAND_NAME;







-- BRAND WISE SALES AND TARGET VALUE

SELECT A.MNYR, 'SALES' TYPE, C.BRAND_CODE, C.BRAND_NAME, SUM(NVL(SAL_VAL,0)) SAL_VAL, SUM(NVL(TAR_VAL,0)) TAR_VAL
FROM(
SELECT TRUNC(INVDATE,'MON') MNYR, P_CODE, ROUND(SUM(ACT-DIS)) SAL_VAL, 0 TAR_VAL 
FROM BMIS_OPL.MIS_MPO_PROD_DD_QV@PRS86
WHERE TRUNC(INVDATE,'MON')  BETWEEN '01-JAN-2025' AND '01-MAR-2025' 
GROUP BY TRUNC(INVDATE,'MON'), P_CODE
UNION ALL
SELECT TRUNC(MNYR,'MON') MNYR, P_CODE, 0 SAL_VAL, ROUND(SUM(ACTVALUE)) TAR_VAL 
FROM BMIS_OPL.MPO_SALES_TAR_SGMT@PRS86
WHERE TRUNC(MNYR,'MON')  BETWEEN '01-JAN-2025' AND '01-MAR-2025'
GROUP BY TRUNC(MNYR,'MON'), P_CODE
) A, PRODUCT B, BRAND C
WHERE A.P_CODE     = B.P_CODE
AND   B.BRAND_CODE = C.BRAND_CODE
AND   B.PROD_GRP IN('G','T','U','I','C','V')
GROUP BY A.MNYR, C.BRAND_CODE, C.BRAND_NAME
ORDER BY MNYR, BRAND_NAME;




-- SALES SKU WISE TARGET

SELECT A.MNYR, A.P_CODE, B.P_DESC, C.BRAND_CODE, C.BRAND_NAME, B.PACK_SIZE1||'X'||B.PACK_SIZE2 PACK, SUM(NVL(A.QNTY,0)) QNTY, ROUND(SUM(A.ACTVALUE)) TAR_VAL, DECODE(NVL(B.PROD_GRP,'G'), 'G', 'GEN-A', 'T', 'GEN-B','U','GEN-C', 'I', 'SPE-E', 'C', 'SPE-D', 'V', 'SPE-F') TEAM 
FROM BMIS_OPL.MPO_SALES_TAR_SGMT@PRS86 A, PRODUCT B, BRAND C
WHERE A.P_CODE     = B.P_CODE
AND   B.BRAND_CODE = C.BRAND_CODE
AND   TRUNC(A.MNYR,'MON') BETWEEN '01-JAN-2025' AND '01-MAR-2025'
AND   B.PROD_GRP IN('G','T','U','I','C','V')
GROUP BY A.MNYR, A.P_CODE, B.P_DESC, C.BRAND_CODE, C.BRAND_NAME, B.PACK_SIZE1||'X'||B.PACK_SIZE2, DECODE(NVL(B.PROD_GRP,'G'), 'G', 'GEN-A', 'T', 'GEN-B','U','GEN-C', 'I', 'SPE-E', 'C', 'SPE-D', 'V', 'SPE-F')
ORDER BY A.MNYR, B.P_DESC;




-- SALES AND PRODUCTION FORECAST

SELECT A.MNYR, A.P_CODE, B.P_DESC, C.BRAND_CODE, C.BRAND_NAME, B.PACK_SIZE1||'X'||B.PACK_SIZE2 PACK, SUM(NVL(A.T_QNTY_FINAL,0)) PROD_FORECAST, SUM(NVL(A.T_QNTY_SALE,0)) SALES_FORECAST, DECODE(NVL(B.PROD_GRP,'G'), 'G', 'GEN-A', 'T', 'GEN-B','U','GEN-C', 'I', 'SPE-E', 'C', 'SPE-D', 'V', 'SPE-F') TEAM 
FROM ERP_OPL.ROLLING_FORECAST A, PRODUCT B, BRAND C
WHERE A.P_CODE     = B.P_CODE
AND   B.BRAND_CODE = C.BRAND_CODE
AND   TRUNC(A.MNYR,'MON')  BETWEEN '01-JAN-2025' AND '01-MAR-2025'
AND   NVL(A.T_QNTY_SALE,0)>0
AND   B.PROD_GRP IN('G','T','U','I','C','V')
GROUP BY A.MNYR, A.P_CODE, B.P_DESC, C.BRAND_CODE, C.BRAND_NAME, B.PACK_SIZE1||'X'||B.PACK_SIZE2, DECODE(NVL(B.PROD_GRP,'G'), 'G', 'GEN-A', 'T', 'GEN-B','U','GEN-C', 'I', 'SPE-E', 'C', 'SPE-D', 'V', 'SPE-F')
ORDER BY A.MNYR, B.P_DESC;



