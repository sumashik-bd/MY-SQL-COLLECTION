CREATE OR REPLACE FUNCTION PBMS_OPL.PPM_TEAM_FUNC(VPPM_REQ_NO VARCHAR2)
RETURN CHAR
IS
    V_TEAM VARCHAR2(2000);
BEGIN
                SELECT DISTINCT LISTAGG(DISTINCT TEAM_NAME, ',') WITHIN GROUP (ORDER BY TEAM_NAME) AS TEAM_NAME
                INTO V_TEAM
                FROM   PPM_REQ_ITEM A,PPM_BRAND_DTL B,PRODUCT C, OPL.TEAM_DESC D,PPM_SUB_TYPE E
				WHERE A.PPM_CODE=B.PPM_CODE
				AND B.P_CODE=C.P_CODE
				AND D.TEAM_STATUS='Y'
				AND B.BRAND_CODE<>'00'
				AND NVL(B.PROD_RATIO,0)>0
				AND D.TEAM_CODE=E.FF_TYPE(+)
				AND C.PROD_GRP=D.TEAM_CODE
				AND A.REQ_NO=VPPM_REQ_NO;
                RETURN V_TEAM;
EXCEPTION
WHEN OTHERS THEN
NULL;
END;
/
