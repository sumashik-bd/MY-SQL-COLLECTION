CREATE TABLE INVOICE_MST (

INV_NO VARCHAR2(20)

);

 

SELECT INV_NO FROM INVOICE_MST;

 

SELECT SUBSTR(INV_NO,9,20) FROM INVOICE_MST;

 

SELECT MAX(SUBSTR(INV_NO,9,20)) FROM INVOICE_MST;

 

SELECT NVL(MAX(SUBSTR(INV_NO,9,20)),'0') FROM INVOICE_MST;

 

SELECT TO_NUMBER(NVL(MAX(SUBSTR(INV_NO,9,20)),'0')) FROM INVOICE_MST;

 

SELECT TO_NUMBER(NVL(MAX(SUBSTR(INV_NO,9,20)),'0'))+1 FROM INVOICE_MST;

 

SELECT LPAD(TO_NUMBER(NVL(MAX(SUBSTR(INV_NO,9,20)),'0'))+1,10,'0') FROM INVOICE_MST;

 

SELECT TO_CHAR(SYSDATE,'YYYYDDMM')||LPAD(TO_NUMBER(NVL(MAX(SUBSTR(INV_NO,9,20)),'0'))+1,10,'0') FROM INVOICE_MST;