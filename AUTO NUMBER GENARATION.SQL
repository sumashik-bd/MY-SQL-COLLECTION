CREATE TABLE INVO_MST (

INV_NO VARCHAR2(10)

);

 

 

SELECT INV_NO FROM INVO_MST;

 

SELECT MAX(INV_NO) FROM INVO_MST;

 

SELECT NVL(MAX(INV_NO),'0') FROM INVO_MST;

 

SELECT TO_NUMBER(NVL(MAX(INV_NO),'0')) FROM INVO_MST;

 

SELECT TO_NUMBER(NVL(MAX(INV_NO),'0'))+1 FROM INVO_MST;

 

SELECT LPAD(TO_NUMBER(NVL(MAX(INV_NO),'0'))+1,10,'0') FROM INVO_MST;

 

 

